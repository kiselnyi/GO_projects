// Вложенные циклы. Вложенные циклы — это циклы, которые находятся внутри других циклов. Они позволяют выполнять более сложные итерации и обрабатывать многомерные структуры данных. Например, можно использовать вложенные циклы для обхода двумерного массива или матрицы. Внутренний цикл будет выполняться полностью для каждой итерации внешнего цикла, что позволяет обрабатывать каждый элемент структуры данных по отдельности.
func nested_loops_example() {
    for i := 1; i < 10; i++{
        for j := 1; j < 10; j++{
            fmt.Print(i * j, "\t")
        }
        fmt.Println()
    }
}
// Этот код выводит таблицу умножения от 1 до 9. Внешний цикл (i) отвечает за строки, а внутренний цикл (j) — за столбцы. Для каждой итерации внешнего цикла внутренний цикл выполняется полностью, выводя произведение i и j, а затем переходит на новую строку после завершения внутреннего цикла.
// Перебор наборов данных (строка и массив). В Go можно использовать цикл for для перебора элементов строки или массива. Для этого используется конструкция range, которая возвращает индекс и значение каждого элемента в наборе данных. Например, можно использовать range для перебора символов в строке или элементов в массиве, что позволяет легко обрабатывать каждый элемент по отдельности.
func iterate_over_string_example() {
	str := "Hello, World!"
	for index, char := range str {
		fmt.Printf("Index: %d, Character: %c\n", index, char)
	}
}
// Этот код перебирает каждый символ строки str и выводит его индекс и сам символ. Конструкция range позволяет легко получить доступ к каждому элементу строки, что упрощает обработку текста.
// Перебор элементов массива с использованием range. Аналогично строкам, можно использовать range для перебора элементов массива. Например, можно создать массив чисел и вывести каждый элемент вместе с его индексом.
